package Comparasion;

public class rationale {
    public static void main(String[] args) {
        
     /*
    
     && --> and 
     Bütün sonuçlar kendi içinde true ise genel sonuç true, en az biri false ise genel sonuç false olur.

     || --> or
     Sonuçlardan en az biri true ise genel sonuç true, hepsi false ise genel sonuç false olur.

      ! --> not

     */

    System.out.println(2 < 3 && 3 > 4);
    System.out.println(! (3 < 4));

    }

}