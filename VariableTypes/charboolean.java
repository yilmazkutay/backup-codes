package VariableTypes;

public class charboolean {
    public static void main(String[] args) {
    // char: karakterleri göstermek için kullanılır.
    // boolean: koşul durumlarında kullanılır, true ve false olmak üzere 2 değeri vardır.

    char a = 'A';

    System.out.println(a);

    boolean c = true;
    boolean d = false;

    System.out.println(c);
    System.out.println(d);

    }

}